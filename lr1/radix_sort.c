/*
 *  Подсчёт элементарных операций (8 типов):
 *    вызов метода, выход из метода, индексация массива,
 *    обращение к полю (->): 2 операции, разыменование (*ptr): 1 операция,
 *    присваивание, сравнение, арифметика.
 *
 *  find_max                : 6n − 1         (худший случай)
 *  counting_sort_by_digit  : 26n + 11k + 10 (k=10: 26n + 120)
 *  radix_sort итого        : 6n + 6 + d·(26n + 11k + 15)
 *
 *  n — число элементов, d — разрядов в max, k = BASE = 10
 */

/* ═══════════════════════════════════════════════════════════
 *  СТАТИЧЕСКИЙ МАССИВ
 *  Память выделяется на стеке. Размер ограничен STATIC_MAX.
 * ═══════════════════════════════════════════════════════════ */

void static_init(StaticArray *sa)
{
    sa->size = 0; /* +2 ссылки (->size), +1 присваивание */
}
/* Итого: 4 операции (2 ссылки + 1 присв. + 1 выход) */

/* Добавить val в конец. Возвращает 0 при успехе, -1 при переполнении. */
int static_push(StaticArray *sa, int val)
{
    if (sa->size >= STATIC_MAX) {          /* +2 ссылки (->size), +1 сравнение */
        fprintf(stderr, "Ошибка: статический массив переполнен (макс. %d)\n",
                STATIC_MAX);               /* +1 вызов */
        return -1;                         /* +1 выход */
    }
    sa->data[sa->size] = val; /* +2 ссылки (->data), +2 ссылки (->size), +1 индексация, +1 присваивание */
    sa->size++;               /* +2 ссылки (->size), +1 арифметика, +1 присваивание */
    return 0;                 /* +1 выход */
}
/* Итого (норм. путь): 14 операций */

/* ═══════════════════════════════════════════════════════════
 *  ДИНАМИЧЕСКИЙ МАССИВ
 *  Выделяем ровно n элементов через malloc — без realloc.
 * ═══════════════════════════════════════════════════════════ */

void dynamic_init(DynamicArray *da)
{
    da->data = NULL; /* +2 ссылки (->data), +1 присваивание */
    da->size = 0;    /* +2 ссылки (->size), +1 присваивание */
}
/* Итого: 7 операций (+ выход из метода) */

/* Выделить память для n элементов. */
static void dynamic_alloc(DynamicArray *da, int n)
{
    da->data = (int *)malloc(n * sizeof(int)); /* +1 арифметика, +1 вызов, +2 ссылки (->data), +1 присваивание */
    if (!da->data) {                           /* +2 ссылки (->data), +1 сравнение */
        fprintf(stderr, "Ошибка: не удалось выделить память\n"); /* +1 вызов */
        exit(EXIT_FAILURE);                    /* +1 вызов */
    }
}
/* Итого (норм. путь): 9 операций (+ выход из метода) */

/* Записать val в конец. При первом вызове выделяет память. */
void dynamic_push(DynamicArray *da, int val, int n)
{
    if (da->data == NULL)      /* +2 ссылки (->data), +1 сравнение */
        dynamic_alloc(da, n);  /* +1 вызов */
    da->data[da->size] = val;  /* +2 ссылки (->data), +2 ссылки (->size), +1 индексация, +1 присваивание */
    da->size++;                /* +2 ссылки (->size), +1 арифметика, +1 присваивание */
}
/* Итого (норм. путь, без вызова alloc): 14 операций (+ выход из метода) */

void dynamic_free(DynamicArray *da)
{
    free(da->data);  /* +2 ссылки (->data), +1 вызов */
    da->data = NULL; /* +2 ссылки (->data), +1 присваивание */
    da->size  = 0;   /* +2 ссылки (->size), +1 присваивание */
}
/* Итого: 10 операций (+ выход из метода) */

/* ═══════════════════════════════════════════════════════════
 *  ВЕКТОР (авторасширяемый динамический массив)
 *  Начальная ёмкость = 4. При заполнении удваивается.
 *  Стратегия удвоения: O(1) амортизированного времени на push.
 * ═══════════════════════════════════════════════════════════ */

#define VECTOR_INIT_CAP 4

void vector_init(Vector *v)
{
    v->data     = NULL; /* +2 ссылки (->data), +1 присваивание */
    v->size     = 0;    /* +2 ссылки (->size), +1 присваивание */
    v->capacity = 0;    /* +2 ссылки (->capacity), +1 присваивание */
}
/* Итого: 10 операций (+ выход из метода) */

/* Добавить val в конец вектора, расширив при необходимости. */
void vector_push(Vector *v, int val)
{
    if (v->size >= v->capacity) {                /* +2 ссылки (->size), +2 ссылки (->capacity), +1 сравнение */
        int new_cap = (v->capacity == 0)         /* +2 ссылки (->capacity), +1 сравнение */
                      ? VECTOR_INIT_CAP
                      : v->capacity * 2;         /* +2 ссылки (->capacity), +1 арифметика */
                                                 /* +1 присваивание (new_cap =) */
        int *tmp = (int *)realloc(v->data,       /* +2 ссылки (->data) */
                         new_cap * sizeof(int)); /* +1 арифметика */
                                                 /* +1 вызов, +1 присваивание (tmp =) */
        if (!tmp) {                              /* +1 сравнение */
            fprintf(stderr, "Ошибка: realloc вернул NULL\n"); /* +1 вызов */
            exit(EXIT_FAILURE);                  /* +1 вызов */
        }
        v->data     = tmp;     /* +2 ссылки (->data), +1 присваивание */
        v->capacity = new_cap; /* +2 ссылки (->capacity), +1 присваивание */
    }
    v->data[v->size] = val; /* +2 ссылки (->data), +2 ссылки (->size), +1 индексация, +1 присваивание */
    v->size++;              /* +2 ссылки (->size), +1 арифметика, +1 присваивание */
}
/* Итого: 16 операций (без realloc);  35 операций (с realloc, не первый вызов);
          32 операции (с realloc, первый вызов — capacity==0, нет умножения) */

void vector_free(Vector *v)
{
    free(v->data);      /* +2 ссылки (->data), +1 вызов */
    v->data     = NULL; /* +2 ссылки (->data), +1 присваивание */
    v->size     = 0;    /* +2 ссылки (->size), +1 присваивание */
    v->capacity = 0;    /* +2 ссылки (->capacity), +1 присваивание */
}
/* Итого: 13 операций (+ выход из метода) */

