/*
 *  read_file — считывание чисел из текстового файла.
 *  Два прохода: первый считает n, второй заполняет структуру.
 *  Итого: 10n + 12 операций (не считая внутренности push-функций).
 */
int read_file(const char *filename, int mode,
              StaticArray *sa, DynamicArray *da, Vector *v)
{
    FILE *fp = fopen(filename, "r"); /* +1 вызов, +1 присваивание */
    if (!fp) {                       /* +1 сравнение */
        fprintf(stderr, "Ошибка: не удалось открыть файл '%s'\n", filename);
        return -1;
    }

    /* Первый проход: подсчёт чисел */
    int n = 0; /* +1 присваивание */
    int tmp;
    while (fscanf(fp, "%d", &tmp) == 1) /* n+1 вызовов, n+1 сравнений */
        n++;                             /* +1 арифметика */

    if (n == 0) { /* +1 сравнение */
        fprintf(stderr, "Ошибка: файл '%s' пуст или не содержит чисел\n", filename);
        fclose(fp);
        return -1;
    }

    rewind(fp); /* +1 вызов */

    /* Второй проход: i=0(+1 присв.); i<n(n+1 сравн.); i++(n арифм.) */
    for (int i = 0; i < n; i++) {
        int val;
        if (fscanf(fp, "%d", &val) != 1) /* +1 вызов, +1 сравнение */
            break;

        if (mode == 1) {                  /* +1 сравнение */
            if (static_push(sa, val) != 0) { fclose(fp); return -1; }
        } else if (mode == 2) {           /* +1 сравнение */
            dynamic_push(da, val, n);
        } else {
            vector_push(v, val);
        }                                 /* +1 вызов (push) */
    }

    fclose(fp); /* +1 вызов */
    return n;   /* +1 выход */
}
/* Итого: 10n + 12 операций */

/*
 *  print_array — вывод элементов массива через пробел.
 *  Итого: 7n + 3 операций.
 */
void print_array(const int *arr, int n)
{
    /* i=0: +1 присв.; i<n: n+1 сравн.; i++: n арифм. */
    for (int i = 0; i < n; i++) {
        printf("%d", arr[i]);  /* +1 индексация, +1 вызов */
        if (i < n - 1)         /* +1 арифметика, +1 сравнение */
            printf(" ");       /* +1 вызов */
    }
    printf("\n"); /* +1 вызов */
}
/* Итого: 7n + 3 операций */

/* ═══════════════════════════════════════════════════════════
 *  RADIX SORT
 * ═══════════════════════════════════════════════════════════ */

#define BASE 10

/* find_max — поиск максимального элемента в arr[0..n-1].
 * Итого: 6n − 1 операций (худший случай). */
int find_max(const int *arr, int n)
{
    int max = arr[0];              /* +1 индексация, +1 присваивание */
    /* i=1: +1 присв.; i<n: n сравн.; i++: n-1 арифм. */
    for (int i = 1; i < n; i++) {
        if (arr[i] > max)          /* +1 индексация, +1 сравнение */
            max = arr[i];          /* +1 индексация, +1 присваивание */
    }
    return max;                    /* +1 выход из метода */
}
/* Итого: 6n − 1 операций (худший случай) */

/*
 *  counting_sort_by_digit — устойчивая сортировка подсчётом
 *  по одному разряду exp (1, 10, 100, …).
 *  Обход справа налево на шаге 4 гарантирует устойчивость.
 *
 *  Подсчёт (k = BASE = 10):
 *    Подготовка (malloc + проверка)   :  4
 *    Шаг 1 (обнуление count[0..k-1]) :  4k + 2   = 42
 *    Шаг 2 (подсчёт вхождений)       :  8n + 2
 *    Шаг 3 (префиксные суммы)        :  7k − 5   = 65
 *    Шаг 4 (расстановка в output[])  :  13n + 3
 *    Шаг 5 (копирование обратно)     :  5n + 2
 *    free + выход                    :  2
 *    ─────────────────────────────────────────────
 *    Итого: 26n + 11k + 10  =  26n + 120  (при k=10)
 */
static void counting_sort_by_digit(int *arr, int n, int exp)
{
    int *output = (int *)malloc(n * sizeof(int)); /* +1 арифметика, +1 вызов, +1 присваивание */
    if (!output) {                                /* +1 сравнение */
        fprintf(stderr, "Ошибка: не удалось выделить память для output\n");
        exit(EXIT_FAILURE);
    }

    int count[BASE];

    /* Шаг 1: i=0(+1 присв.); i<k: k+1 сравн.; i++: k арифм.;
              count[i](+1 индекс.), =0(+1 присв.) — k раз
              Итого шаг 1: 4k + 2 = 42 */
    for (int i = 0; i < BASE; i++)
        count[i] = 0; /* +1 индексация, +1 присваивание */

    /* Шаг 2: i=0(+1 присв.); i<n: n+1 сравн.; i++: n арифм.;
              arr[i](+1 индекс.), /exp(+1 арифм.), %BASE(+1 арифм.),
              digit=(+1 присв.), count[digit](+1 индекс.), ++(+1 арифм.) — n раз
              Итого шаг 2: 8n + 2 */
    for (int i = 0; i < n; i++) {
        int digit = (arr[i] / exp) % BASE; /* +1 индексация, +1 арифметика, +1 арифметика, +1 присваивание */
        count[digit]++;                    /* +1 индексация, +1 арифметика */
    }

    /* Шаг 3: i=1(+1 присв.); i<k: k сравн.; i++: k-1 арифм.;
              count[i](+1 индекс.), i-1(+1 арифм.), count[i-1](+1 индекс.),
              +=(+1 арифм.+1 присв.) — (k-1) раз
              Итого шаг 3: 7k − 5 = 65 */
    for (int i = 1; i < BASE; i++)
        count[i] += count[i - 1]; /* +1 индексация, +1 арифметика, +1 индексация, +1 арифметика, +1 присваивание */

    /* Шаг 4: i=n-1(+1 арифм.+1 присв.); i>=0: n+1 сравн.; i--: n арифм.;
              arr[i](+1 инд.), /exp(+1 арифм.), %BASE(+1 арифм.), digit=(+1 присв.),
              count[digit](+1 инд.), -1(+1 арифм.), output[...](+1 инд.),
              arr[i](+1 инд.), =(+1 присв.), count[digit](+1 инд.), --(+1 арифм.) — n раз
              Итого шаг 4: 13n + 3 */
    for (int i = n - 1; i >= 0; i--) {
        int digit = (arr[i] / exp) % BASE;  /* +1 индексация, +1 арифметика, +1 арифметика, +1 присваивание */
        output[count[digit] - 1] = arr[i];  /* +1 индексация, +1 арифметика, +1 индексация, +1 индексация, +1 присваивание */
        count[digit]--;                      /* +1 индексация, +1 арифметика */
    }

    /* Шаг 5: i=0(+1 присв.); i<n: n+1 сравн.; i++: n арифм.;
              arr[i](+1 инд.), output[i](+1 инд.), =(+1 присв.) — n раз
              Итого шаг 5: 5n + 2 */
    for (int i = 0; i < n; i++)
        arr[i] = output[i]; /* +1 индексация, +1 индексация, +1 присваивание */

    free(output); /* +1 вызов */
}
/* Итого: 26n + 11k + 10 операций  (k=10: 26n + 120) */

/*
 *  radix_sort — цифровая сортировка LSD.
 *  Поддерживает отрицательные числа: перед сортировкой все элементы
 *  сдвигаются на |min| вверх, после сортировки — сдвигаются обратно.
 *  Выполняет d проходов counting_sort_by_digit по разрядам
 *  exp = 1, 10, 100, … пока max / exp > 0.
 */
void radix_sort(int *arr, int n)
{
    if (n <= 1) return; /* +1 сравнение, +1 выход */

    /* Найти минимальный элемент */
    int min_val = arr[0];
    for (int i = 1; i < n; i++)
        if (arr[i] < min_val) min_val = arr[i];

    /* Если есть отрицательные — сдвинуть все на |min_val| */
    if (min_val < 0) {
        int shift = -min_val;
        for (int i = 0; i < n; i++)
            arr[i] += shift;
    }

    int max_val = find_max(arr, n); /* +1 вызов, +1 присваивание */

    /* exp=1(+1 присв.); max/exp(+1 арифм.) >0(+1 сравн.): d+1 раз;
       exp*=BASE(+1 арифм.+1 присв.): d раз;
       вызов counting_sort: d раз */
    for (int exp = 1; max_val / exp > 0; exp *= BASE)
        counting_sort_by_digit(arr, n, exp);

    /* Сдвинуть обратно */
    if (min_val < 0) {
        int shift = -min_val;
        for (int i = 0; i < n; i++)
            arr[i] -= shift;
    }
}
/* Итого (собственный код radix_sort): 7 + 5d операций (без учёта сдвига)
   Итого полностью (с подфункциями):
     6n + 6 + d·(26n + 11k + 15)  =  6n + 6 + d·(26n + 125)  при k=10 */
