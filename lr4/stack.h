#ifndef STACK_H
#define STACK_H

/*
 * Заголовочный файл лабораторной работы №4.
 *
 * Определяет:
 *   - Стек на базе динамического массива (основная структура)
 *   - Интерфейс цифровой сортировки (LSD через 10 стеков-корзин)
 *   - Поиск методом Бойера-Мура с возвратом позиции числа
 *   - Простое красно-чёрное дерево для записи результата
 *
 * Реализация — в stack.c (все три логических блока в одном файле,
 * как требовалось в предыдущих лабораторных).
 */

#include <stddef.h>

/* ===================== СТЕК НА БАЗЕ ДИНАМИЧЕСКОГО МАССИВА ===================== */

/* Стек LIFO. Дно — data[0], вершина — data[top].
 * При переполнении ёмкость удваивается (амортизировано O(1) на push). */
typedef struct {
    int *data;     /* буфер на куче */
    int  top;      /* индекс вершины; -1 — стек пуст */
    int  capacity; /* выделенная ёмкость */
} Stack;

/* Инициализация пустого стека */
void stack_init(Stack *s);

/* Проверка на пустоту */
int  stack_is_empty(const Stack *s);

/* Количество элементов */
int  stack_size(const Stack *s);

/* Добавление элемента на вершину. Возвращает 0 при успехе, -1 при ошибке памяти. */
int  stack_push(Stack *s, int value);

/* Снятие элемента с вершины. Возвращает 0 при успехе, -1 если пуст. Значение через *out. */
int  stack_pop(Stack *s, int *out);

/* Просмотр вершины без удаления. Возвращает 0 при успехе, -1 если пуст. */
int  stack_peek(const Stack *s, int *out);

/* Освобождение памяти буфера (саму структуру не free) */
void stack_free(Stack *s);

/* ===================== ЦИФРОВАЯ СОРТИРОВКА (LSD) ===================== */

/* Цифровая сортировка стека по возрастанию (от дна к вершине).
 * Поддерживает отрицательные числа.
 * После сортировки: data[0] — наименьший, data[top] — наибольший. */
void radix_sort(Stack *s);

/* ===================== ПОИСК МЕТОДОМ БОЙЕРА-МУРА ===================== */

/* Поиск методом Бойера-Мура.
 * Строит текстовую строку из элементов стека (от дна к вершине) в формате ";число;число;...;".
 * Подпоследовательность keys[0..keys_len-1] преобразуется в паттерн ";k0;k1;...;kN-1;".
 * Возвращает позицию первого числа найденной подпоследовательности в стеке (0 — дно)
 * или -1, если подпоследовательность не найдена.
 * В *views — количество сравнений символов, выполненных алгоритмом Бойера-Мура. */
int boyer_moore_search(const Stack *s, const int *keys, int keys_len, int *views);

/* ===================== КРАСНО-ЧЁРНОЕ ДЕРЕВО ===================== */

typedef enum { BLACK, RED } Color;

typedef struct RBNode {
    int value;
    Color color;
    struct RBNode *left;
    struct RBNode *right;
    struct RBNode *parent;
} RBNode;

typedef struct {
    RBNode *root;
} RBTree;

/* Инициализация пустого дерева */
void rbtree_init(RBTree *t);

/* Вставка значения (дубликаты допускаются) */
void rbtree_insert(RBTree *t, int value);

/* Вывод значений дерева в порядке inorder (должен совпадать с отсортированным стеком) */
void rbtree_inorder_print(const RBTree *t);

/* Визуализация дерева в терминале: правый — вверх, левый — вниз; [R/B:значение] */
void rbtree_print_visual(const RBTree *t);

/* Освобождение всех узлов дерева */
void rbtree_free(RBTree *t);

#endif /* STACK_H */